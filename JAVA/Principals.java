//Single responsibility is that one class has one job to do.
//So when we are going to change we have to make only one change and we dont get confused.

//Open-closed is that software entities should be open for extension, but closed for modification.
//As a result, when the business requirements change then the entity can be extended, but not modified.


//Liskov substitution is that objects of a superclass shall be replaceable with objects of its subclasses without breaking the application.
//That requires the objects of your subclasses to behave in the same way as the objects of your superclass.


//Interface substitution is that no client should be forced to depend on methods it does not use.
//Imagine an interface with many methods in our codebase and that many of our classes implement this interface, although only some of its methods are implemented


//Dependency inversion is that high-level modules should not depend upon low-level modules they should depend on abstractions.
//Secondly, abstractions should not depend upon details; details should depend upon abstractions.
